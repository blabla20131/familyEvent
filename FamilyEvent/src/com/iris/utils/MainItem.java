package com.iris.utils;

public class MainItem {
	
	public String title;
	public String date;
	public String area;
	public String category;
	public String phoneNumber;
	public int amount;
	
	public MainItem(String title, String date, String area, String category, String phoneNumber, int amount)
	{
		this.title = title;
		this.date = date;
		this.area = area;
		this.category = category;
		this.phoneNumber = phoneNumber;
		this.amount = amount;
	}
}
